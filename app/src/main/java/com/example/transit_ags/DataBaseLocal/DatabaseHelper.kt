    // Mtodo para obtener los puntos de una ruta
    fun getPuntosDeRuta(nombreRuta: String): List<Ruta> {
        val db = readableDatabase
        val cursor = db.query(
            Constants.TABLE_RUTA, // Nombre de la tabla
            null,       // Selecciona todas las columnas
            "Nombre = ?",  // Condicin para filtrar por nombre de ruta
            arrayOf(nombreRuta),  // Parmetros para la condicin
            null, null, "No_Punto ASC" // Ordenar por No_Punto
        )

        val rutaList = mutableListOf<Ruta>()

        // Iterar sobre el cursor y obtener los resultados
        while (cursor.moveToNext()) {
            val idRutaIndex = cursor.getColumnIndex("id_Ruta")
            val nombreIndex = cursor.getColumnIndex("Nombre")
            val sentidoIndex = cursor.getColumnIndex("Sentido")
            val noPuntoIndex = cursor.getColumnIndex("No_Punto")
            val latitudIndex = cursor.getColumnIndex("Latitud")
            val longitudIndex = cursor.getColumnIndex("Longitud")

            // Validar que los ndices son vlidos (>= 0)
            if (idRutaIndex >= 0 && nombreIndex >= 0 && sentidoIndex >= 0 &&
                noPuntoIndex >= 0 && latitudIndex >= 0 && longitudIndex >= 0) {
                val idRuta = cursor.getInt(idRutaIndex)
                val nombre = cursor.getString(nombreIndex)
                val sentido = cursor.getString(sentidoIndex)
                val noPunto = cursor.getInt(noPuntoIndex)
                val latitud = cursor.getDouble(latitudIndex)
                val longitud = cursor.getDouble(longitudIndex)

                val ruta = Ruta(idRuta, nombre, sentido, noPunto, latitud, longitud)
                rutaList.add(ruta)
            } else {
                Log.e("SQLite", "Alguna columna no se encontr en el cursor")
            }
        }
        cursor.close()
        return rutaList
    }

    fun getParadas(): List<LatLng> {
        val paradas = mutableListOf<LatLng>()
        val db = readableDatabase
        val cursor = db.rawQuery("SELECT Latitud, Longitud FROM ${Constants.TABLE_PARADA}", null)

        while (cursor.moveToNext()) {
            val latitud = cursor.getDouble(0)
            val longitud = cursor.getDouble(1)
            paradas.add(LatLng(latitud, longitud))
        }

        cursor.close()
        db.close()
        return paradas
    }



